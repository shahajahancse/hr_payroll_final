ছেলেমেয়ে নির্বিশেষে ১৮ বছরের নীচে কোন ব্যক্তিকে কারখানার কোন ধরণের কাজে বা শ্রমে নিয়োজিত করা যাবে না। বয়সের নির্ভূলতা নিশ্চিত করার জন্য শিক্ষাগত সনদপত্র/জন্ম নিবন্ধন সনদপত্র/ইউনিয়ন পরিষদ/পৌরসভা/ভোটার আইডি কার্ড/আঞ্চলিক কর্তৃপক্ষ কর্তৃক সনদপত্র এবং রেজিষ্টার্ড ডাক্তার কর্তৃক প্রদত্ত বয়স সংক্রান্ত সনদপত্র সংগ্রহ করা এবং তা শ্রমিকদের ব্যক্তিগত নথি ফাইলে সংরক্ষণ করা বাধ্যতামূলক। <br />
<br />
<div align="center"> <b style="font-size: 12px"> 	বাধ্যতামূলক শ্রম :</b></div><br />
কোম্পানী দেশের শ্রম আইন মোতাবেক যে কোন ধরনের বাধ্যতামূলক শ্রম যেমন প্রিজন লেবার, ইনডেনজার্ড লেবার, বন্ডেড লেবার ইত্যাদি থেকে সম্পূর্ণ বিরত থাকে। কাজের ব্যাপারে কোন শ্রমিককে জোরপূর্বক বাধ্য করা হয় না।  </div>
<div style="width: 305px;float: left;height: 450px;border:solid 1px; margin:20px;margin-top: 70px;" > 
	<div align="center"> <b style="font-size: 12px"> 	সম-অধিকার :</b></div>
চাকুরীর জন্য নিয়োগ ও নির্বাচনের ক্ষেত্রে জাতি, ধর্ম, বর্ণ, গোত্র, লিঙ্গ, বয়স, জাতীয়তা, বৈবাহিক অবস্থা, রাজনীতি মতভেদ ইত্যাদি নির্বিশেষে প্রত্যক্ষ বা পরোক্ষ যে কোন ধরনের বৈষম্যমূলক আচরণ ও বিবেচনা থেকে কোম্পানী সম্পূর্ণরূপে বিরত থাকে এবং নিরপেক্ষ নীতি মেনে চলে। 

<div align="center"> <b style="font-size: 12px"> 	হয়রানী ও গালমন্দ  :</b></div>
কোন শ্রমিক/কর্মচারী যে কোন ধরণের দৈহিক, মানসিক, মৌখিক, যৌন হয়রানী বা অপব্যবহারের শিকার না হয়, সে বিষয়ে কোম্পানী নিশ্চিত করে। 

<div align="center"> <b style="font-size: 12px"> 	স্বাস্থ্য ও নিরাপত্তা  :</b></div>
কোম্পানী একটি পূর্ণাঙ্গ নিয়মনীতি, অবকাঠামো ও সংস্কৃতি অনুসরণ ও সংরক্ষণের মাধ্যমে কর্মক্ষেত্রে শ্রমিক ও কর্মচারীদের সামগ্রিক স্বাস্থ্য ও নিরাপত্তা সংক্রান্ত সচেতনতা বৃদ্ধিকল্পে প্রয়োজনীয় ব্যবস্থা নেয় ও উদ্বুদ্ধ করে। কোম্পানী নিয়মিত পর্যবেক্ষণ, বিশ্লেষণ, মূল্যায়ণ ও অভিজ্ঞতার আলোকে প্রচলিত স্বাস্থ্য ও নিরাপত্তা বিধি বিধানের অধিকতর উৎকর্ষ সাধনে প্রয়োজনীয় পদক্ষেপ গ্রহণে মনোযোগী থাকে।</div>
<div style="width: 305px;float: left;height: 450px;border:solid 1px; margin:20px;margin-top: 70px;" >
	একটি কার্যকরী, বাস্তবসম্মত স্বাস্থ্য ও নিরাপত্তা সংক্রান্ত নিয়মনীতির মান বজায় রাখতে কোম্পানী প্রতিনিয়ত উৎকর্ষ সাধনে পদক্ষেপ গ্রহণ করে এবং কোম্পানীকে একটি আদর্শ শিল্প প্রতিষ্ঠানরূপে গড়ে তুলতে সচেষ্ট থাকে। <br />
স্বাস্থ্য ও নিরাপত্তা সংক্রান্ত বিধিমালার ক্ষেত্রে কোম্পানী সবসময় দেশের প্রচলিত আইন এবং সংশ্লিষ্ট আন্তর্জাতিক নীতিমালা অনুসরণ করে চলে। যে ক্ষেত্রে কোন নীতিমালা নেই সে ক্ষেত্রে কোম্পানী নিজস্ব মানদন্ড প্রণয়ন ও প্রয়োগের ব্যবস্থা করে।  <br />

কর্মক্ষেত্রে সকল প্রকার সম্ভাব্য ঝুঁকিপূর্ণ উৎস নির্ণয় এবং তার যথাযথ প্রতিকারকল্পে কোম্পানী সর্বদাই প্রয়োজনীয় পদক্ষেপ ও উদ্যোগ গ্রহণের মাধ্যমে নিরাপদ ও স্বাস্থ্যসম্মত কর্ম পরিবেশ সৃষ্টিতে সচেষ্ট থাকে। একটি গ্রহণযোগ্য নিরাপদ ও স্বাস্থ্যসম্মত কর্মপরিবেশ সৃষ্টিকল্পে কোম্পানীতে নিয়োজিত সকল প্রকার শ্রমিক, কর্মচারী এবং কোম্পানীর সহিত প্রত্যক্ষ বা পরোক্ষভাবে জড়িত সকল বহিরাগতদের উপযুক্ত জ্ঞানদানে প্রয়োজনীয় প্রশিক্ষণ ও তা মেনে চলার ব্যবস্থা গ্রহণে কোম্পানী সর্বদাই সক্রিয় ভূমিকা পালন করে। 
 </div>
