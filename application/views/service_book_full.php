<div align="center"> <b style="font-size: 12px"> 	বেতন ও সুবিধাদি  :</b></div>
গণপ্রজাতন্ত্রী বাংলাদেশ সরকারের পক্ষ থেকে বিভিন্ন সময় প্রকাশিত শ্রম ও কর্মসংস্থান মন্ত্রণালয় কর্তৃক ন্যূনতম মজুরী অধ্যাদেশের অনুমোদন মোতাবেক নোটিফিকেশন অনুযায়ী কোম্পানী তার কর্মচারীদের বেতন ভাতা প্রদান করে বা নির্ধারণ করে থাকে। 

<div align="center"> <b style="font-size: 12px"> 	কাজের পরিবেশ :</b></div>
প্রতিষ্ঠানের সকল শ্রমিকদের সাথে পূর্ণ মর্যাদা এবং শ্রদ্ধা সহকারে ব্যবহার এবং তাদের জন্য একটি নিরাপদ ও স্বাস্থ্যসম্মত কাজের পরিবেশ নিশ্চিত করে এবং কাজের পরিবেশ সংক্রান্ত সকল নিয়ম নীতিমালা পালন করে। প্রতিষ্ঠান যে কোন ধরণের শারীরিক এবং মানসিক নির্যাতন থেকে বিরত থাকা নিশ্চিত করে। 
